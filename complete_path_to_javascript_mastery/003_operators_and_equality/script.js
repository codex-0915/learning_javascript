/* Arithmetic Operators */
const num1 = 124;
const num2 = 3;
let result = 0;
// Addition
result = num1 + num2;
// Subtraction
result = num1 - num2;
// Multiplication
result = num1 * num2;
// Division
result = num1 / num2;
// Exponent
result = num1 ** num2;
// Modulo
result = num1 % num2;
// Increment & Decrement
result++;
result--;
console.log(result);